package block1.methodsFunctions;

public class sumNumbers {
    public static void main(String[] args) {
        int a1 = 10;
        int a2 = 7;
        int sum = sumNumbers(a1, a2);
        System.out.println(sum);
    }
    public static int sumNumbers(int a1, int a2) {
        return a1 + a2;
    }
}
